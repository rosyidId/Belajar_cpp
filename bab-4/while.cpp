#include <iostream>

int main() {   
   int i;
   
   // mengulang aksi sebanyak 5 kali
   i = 0;
   while (i<5) {
      std::cout<<i<<" ";  // menampilkan nilai i dan satu spasi
      i++;	// menaikkan nilai i
   }   
   
   return 0; 
}
